//---------------------------------------------------------------------------


bool Agent::isClosedTo(Agent ag, double distance)
{
	bool close = false;
	double norm = 0; //distance between the two agents

	norm = sqrt( (x-ag.getx())* (x-ag.getx()) + (y-ag.gety())*(y-ag.gety()) );
	
	if( norm < distance)
	{
		close = true;
	}
	

	return close;
}

