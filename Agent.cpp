const double Agent::XMAX = 640;
const double Agent::YMAX = 480;

// ===========================================================================
//                                  Constructors
// ===========================================================================

/* create an Agent with a random positon and a null velocity */
Agent::Agent(void)
{
	
	x = (double)(rand()/(double)RAND_MAX)*XMAX;
	y = (double)(rand()/(double)RAND_MAX)*YMAX;

	vx = 0;
	vy = 0;

}



// ===========================================================================
//                                  Destructor
// ===========================================================================
Agent::~Agent(void)
{
}

// ===========================================================================
//                                 Public Methods
// ===========================================================================


double Agent::getx() 
{
	return x;
}

double Agent::gety()
{
	return y;

}  


double Agent::getVx()
{
	return vx;
} 

double Agent::getVy() 
{
	return vy;
}


